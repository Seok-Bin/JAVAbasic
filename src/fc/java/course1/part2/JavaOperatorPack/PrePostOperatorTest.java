package fc.java.course1.part2.JavaOperatorPack;

public class PrePostOperatorTest {
    public static void main(String[] args) {
        int x = 10;
        System.out.println(--x);
        System.out.println(x++);
        System.out.println(x--);
        System.out.println(++x);
        System.out.println(x);
    }
}
